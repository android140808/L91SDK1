package cn.appscomm.pedometer.model;

public class CircleRing {

	private double realStep;
	private double targetStep;

	private double realDist;
	private double targetDist;

	private double realCal;
	private double targetCal;

	private double realSleep;
	private double targetSleep;

	public double getRealStep() {
		return realStep;
	}

	public void setRealStep(double realStep) {
		this.realStep = realStep;
	}

	public double getTargetStep() {
		return targetStep;
	}

	public void setTargetStep(double targetStep) {
		this.targetStep = targetStep;
	}

	public double getRealDist() {
		return realDist;
	}

	public void setRealDist(double realDist) {
		this.realDist = realDist;
	}

	public double getTargetDist() {
		return targetDist;
	}

	public void setTargetDist(double targetDist) {
		this.targetDist = targetDist;
	}

	public double getRealCal() {
		return realCal;
	}

	public void setRealCal(double realCal) {
		this.realCal = realCal;
	}

	public double getTargetCal() {
		return targetCal;
	}

	public void setTargetCal(double targetCal) {
		this.targetCal = targetCal;
	}

	public double getRealSleep() {
		return realSleep;
	}

	public void setRealSleep(double realSleep) {
		this.realSleep = realSleep;
	}

	public double getTargetSleep() {
		return targetSleep;
	}

	public void setTargetSleep(double targetSleep) {
		this.targetSleep = targetSleep;
	}

}
